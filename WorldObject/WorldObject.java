package WorldObject ;


public class WorldObject{

    
    
    private float x,y,s ; 
    
    
    
    
    
    public WorldObject(){
    this.x=0;
    this.y=0;
    
    }
    
    
    
    public WorldObject (float x,float y){
    this.x=x;
    this.y=y;
    this.s=0;
    }
    
     public WorldObject (float x,float y,float s){
    this.x=x;
    this.y=y;
    this.s=s;
    }
    
    
    
    


}